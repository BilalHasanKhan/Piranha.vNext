using System;

namespace Piranha.Server
{
	/// <summary>
	/// Interface for defining a handler that should answer to 
	/// incoming requests.
	/// </summary>
	public interface IHandler
	{
		/// <summary>
		/// Tries to handle an incoming request.
		/// </summary>
		/// <param name="api">The current api</param>
		/// <param name="request">The incoming route request</param>
		/// <returns>The result</returns>
		IResponse Handle(Api api, IRequest request);
	}
}
